    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        Funciones funciones = new Funciones();

        if (txt_userlogin.getText().equals("Usuario") || txt_passlogin.getText().equals("Contrase√±a")) {
            JOptionPane.showMessageDialog(this, "Ingrese los datos");
        } else {
            try {
                if (funciones.buscarUser(txt_userlogin.getText(), txt_passlogin.getText())) {
                    FrmDatosPrenda datosPrenda = new FrmDatosPrenda();
                    datosPrenda.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Nombre o contrase√±a incorrectos, registrate plis");
                }
            } catch (IOException | ClassNotFoundException ex) {
            }
        }
    }//GEN-LAST:event_jButtonNextActionPerformed

